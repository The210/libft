int	ft_is_prime(int nb)
{
	int a;
	int b;

	a = 2;
	b = 0;
	if (nb <= 1)
		b = 1;
	while ((a * a) <= nb)
	{
		if (nb % a == 0)
			b = 1;
		a++;
	}
	if (b == 0)
		return (1);
	else
		return (0);
}
